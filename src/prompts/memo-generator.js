/**
 * Builds the prompt for Claude to generate a full Investment Committee (IC) memo.
 * Returns clean markdown suitable for direct sharing with an IC.
 */
export function buildMemoGeneratorPrompt(data) {
  const {
    company,
    description,
    sector,
    stage,
    founders,
    marketSize,
    traction,
    competition,
    fundThesis,
    matchScore,
    geography,
    askAmount,
    businessModel,
  } = data;

  return `You are a senior venture capital partner writing an Investment Committee memo. Write a professional, rigorous IC memo for the deal below.

DEAL INFORMATION:
- Company: ${company}
- Description: ${description}
- Sector: ${sector}
- Stage: ${stage}
- Geography: ${geography || 'Not specified'}
- Ask Amount: ${askAmount || 'Not specified'}
- Founders: ${founders || 'Not specified'}
- Market Size: ${marketSize || 'Not specified'}
- Traction: ${traction || 'Not specified'}
- Business Model: ${businessModel || 'Not specified'}
- Competition: ${competition || 'Not specified'}

FUND CONTEXT:
- Fund Thesis: ${fundThesis?.thematicFocus || 'Not specified'}
- Thesis Alignment Score: ${matchScore || 'Not scored'}/100

Write a complete IC memo in clean markdown. Be analytical, direct, and honest about both the opportunity and the risks. Do not be promotional — ICs need balanced analysis.

Use exactly these sections:

# IC Memo: ${company}

## Executive Summary
[2-3 paragraphs covering what the company does, why it matters, and the investment thesis in one punch line]

## Thesis Alignment
[How this deal fits (or doesn't) the fund thesis. Reference the alignment score if provided. Be specific.]

## Team & Founders
[Assessment of the founding team — domain expertise, execution track record, unfair advantages]

## Market Opportunity
[TAM/SAM/SOM breakdown, market dynamics, why this market is attractive now]

## Competitive Landscape
[Main competitors, differentiation, defensibility, moat analysis]

## Traction & Financials
[Current metrics, growth trajectory, unit economics if known, key milestones]

## Key Risks
[3-5 specific risks as a bulleted list, each with a brief mitigation note]

## Recommendation & Terms
[Pass/Pursue recommendation with rationale, suggested check size and terms if pursuing]

---
*Generated by Agentic FA — AI Deal Copilot*`;
}
